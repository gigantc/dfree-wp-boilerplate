#!/usr/bin/env node

/**
 * Generate _blocks.scss from all block and component SCSS partials
 * Replaces the Gulp generateBlocksScssTask
 */

const fs = require('fs');
const path = require('path');

const blocksDir = path.join(__dirname, '../blocks');
const componentsDir = path.join(__dirname, '../components');
const outputFile = path.join(__dirname, '../src/scss/_blocks.scss');

/**
 * Recursively find all SCSS partials in blocks directory
 */
function findScssPartials(dir) {
  let forwards = [];

  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    const entryPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      forwards.push(...findScssPartials(entryPath));
    } else if (
      entry.isFile() &&
      entry.name.startsWith('_') &&
      entry.name.endsWith('.scss')
    ) {
      const relative = path.relative(path.join(__dirname, '../src/scss'), entryPath);
      const cleaned = relative
        .replace(/^_/, '')         // Remove underscore
        .replace(/\.scss$/, '')    // Remove .scss
        .replace(/\\/g, '/');      // Normalize Windows slashes

      forwards.push(`@forward '${cleaned}';`);
    }
  }

  return forwards;
}

// Generate the file
const blockLines = findScssPartials(blocksDir);

// Also scan components directory if it exists
let componentLines = [];
if (fs.existsSync(componentsDir)) {
  componentLines = findScssPartials(componentsDir);
}

const headerComment = `// This file is auto-generated by npm run blocks:generate
// Do not edit this file directly—any changes will be overwritten.\n\n`;

let output = headerComment;

// Add block styles
if (blockLines.length > 0) {
  output += '// Block Styles\n';
  output += blockLines.join('\n') + '\n';
}

// Add component styles
if (componentLines.length > 0) {
  output += '\n// Component Styles\n';
  output += componentLines.join('\n') + '\n';
}

fs.writeFileSync(outputFile, output);

const totalEntries = blockLines.length + componentLines.length;
console.log(`✅ Updated _blocks.scss with ${blockLines.length} blocks and ${componentLines.length} components (${totalEntries} total entries)`);
