#!/usr/bin/env node

/**
 * Generate _blocks.scss from all block and component SCSS partials
 * Replaces the Gulp generateBlocksScssTask
 */

const fs = require('fs');
const path = require('path');

const blocksDir = path.join(__dirname, '../blocks');
const componentsDir = path.join(__dirname, '../components');
const singlesDir = path.join(__dirname, '../singles');
const outputFile = path.join(__dirname, '../src/scss/_blocks.scss');

/**
 * Recursively find all SCSS partials in a directory
 * Validates file existence before adding to output
 */
function findScssPartials(dir, type = 'block') {
  let forwards = [];
  let warnings = [];

  if (!fs.existsSync(dir)) {
    return { forwards, warnings };
  }

  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    const entryPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      const result = findScssPartials(entryPath, type);
      forwards.push(...result.forwards);
      warnings.push(...result.warnings);
    } else if (
      entry.isFile() &&
      entry.name.startsWith('_') &&
      entry.name.endsWith('.scss')
    ) {
      // Validate file exists and is readable
      try {
        fs.accessSync(entryPath, fs.constants.R_OK);

        // Check file is not empty
        const stats = fs.statSync(entryPath);
        if (stats.size === 0) {
          warnings.push(`⚠️  Empty file: ${entryPath}`);
          continue;
        }

        const relative = path.relative(path.join(__dirname, '../src/scss'), entryPath);
        const cleaned = relative
          .replace(/^_/, '')         // Remove underscore
          .replace(/\.scss$/, '')    // Remove .scss
          .replace(/\\/g, '/');      // Normalize Windows slashes

        forwards.push({
          path: cleaned,
          fullPath: entryPath,
          statement: `@forward '${cleaned}';`
        });
      } catch (err) {
        warnings.push(`⚠️  Cannot read file: ${entryPath}`);
      }
    }
  }

  return { forwards, warnings };
}

/**
 * Validate SCSS forward paths don't have obvious issues
 */
function validateForwards(forwards) {
  const issues = [];

  forwards.forEach(f => {
    // Check for paths that might cause issues
    if (f.path.includes(' ')) {
      issues.push(`⚠️  Path contains spaces (may cause issues): ${f.path}`);
    }
  });

  return issues;
}

// Generate the file
const blockResult = findScssPartials(blocksDir, 'block');
const componentResult = findScssPartials(componentsDir, 'component');
const singleResult = findScssPartials(singlesDir, 'single');

// Combine warnings
const allWarnings = [...blockResult.warnings, ...componentResult.warnings, ...singleResult.warnings];

// Validate paths
const blockValidation = validateForwards(blockResult.forwards);
const componentValidation = validateForwards(componentResult.forwards);
const singleValidation = validateForwards(singleResult.forwards);
allWarnings.push(...blockValidation, ...componentValidation, ...singleValidation);

// Print warnings if any
if (allWarnings.length > 0) {
  allWarnings.forEach(w => console.log(w));
}

const headerComment = `// This file is auto-generated by npm run blocks:generate
// Do not edit this file directly—any changes will be overwritten.\n\n`;

let output = headerComment;

// Add block styles
if (blockResult.forwards.length > 0) {
  output += '// Block Styles\n';
  output += blockResult.forwards.map(f => f.statement).join('\n') + '\n';
}

// Add component styles
if (componentResult.forwards.length > 0) {
  output += '\n// Component Styles\n';
  output += componentResult.forwards.map(f => f.statement).join('\n') + '\n';
}

// Add single/section styles
if (singleResult.forwards.length > 0) {
  output += '\n// Single Page Section Styles\n';
  output += singleResult.forwards.map(f => f.statement).join('\n') + '\n';
}

// Write output file
try {
  fs.writeFileSync(outputFile, output);
  const totalEntries = blockResult.forwards.length + componentResult.forwards.length + singleResult.forwards.length;
  console.log(`✅ Updated _blocks.scss with ${blockResult.forwards.length} blocks, ${componentResult.forwards.length} components, and ${singleResult.forwards.length} singles (${totalEntries} total entries)`);
} catch (err) {
  console.error(`❌ Failed to write _blocks.scss: ${err.message}`);
  process.exit(1);
}
